public class task2 {
    public static double avg(double a, double b, double c) {
        return (a + b + c) / 3;
    }

    public static void main(String[] args) {
        double a = 25;
        double b = 45;
        double c = 65;
        System.out.println(avg(a, b, c));
    }
}
